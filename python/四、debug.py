# 1、错误
# 程序错误（program error)是指在程序运行过程中出现的问题，导致程序无法正常执行或产生意外结果。常见的错误类型有3种：语法错误（syntax error)、运行时错误(runtime error)和逻辑错误（logical error)。
# 1.1 语法错误 (Syntax Error)
# 语法错误是指代码不符合编程语言的语法规则，导致解释器或编译器无法理解代码。例如，缺少括号、拼写错误或使用了保留字等都会引发语法错误。语法错误通常在代码编译或解释阶段被检测到，程序无法运行直到错误被修正。
# 示例：
# print("Hello, World!"  # 缺少右括号，导致语法错误
# 1.2 运行时错误 (Runtime Error)
# 运行时错误是在程序运行过程中发生的错误，通常是由于非法操作或资源不可用引起的。例如，除以零、访问不存在的文件或使用未初始化的变量等都会导致运行时错误。运行时错误通常在程序执行时被检测到，可能会导致程序崩溃或异常终止。
# 示例：
# x = 10 / 0  # 除以零，导致运行时错误
# 1.3 逻辑错误 (Logical Error)
# 逻辑错误是指程序虽然能够正常运行，但其行为或结果与预期不符。逻辑错误通常是由于程序设计或算法实现上的缺陷引起的，可能导致错误的计算结果或不正确的程序行为。逻辑错误通常难以检测，因为程序不会崩溃，但结果却是错误的。
# 示例：
# def add(a, b):
#     return a - b  # 错误的实现，应该是加法而不是减法
# result = add(5, 3)  # 结果为2，而不是预期的8在xa